  u.id,
  u.email,
  coalesce(u.raw_user_meta_data ->> 'full_name', u.raw_user_meta_data ->> 'name', split_part(u.email, '@', 1)),
  coalesce(u.raw_user_meta_data ->> 'avatar_url', u.raw_user_meta_data ->> 'picture'),
  coalesce(u.raw_app_meta_data ->> 'provider', 'google'),
  case
    when lower(u.email) = (
      select lower(value) from private.app_config where key = 'initial_admin_email'
    ) then 'admin'::public.app_role
    else 'user'::public.app_role
  end,
  now()
from auth.users u
where u.email is not null
on conflict (id) do update set
  email = excluded.email,
  display_name = coalesce(public.profiles.display_name, excluded.display_name),
  avatar_url = coalesce(excluded.avatar_url, public.profiles.avatar_url),
  provider = excluded.provider;

create or replace function private.is_active_user(requested_user_id uuid default auth.uid())
returns boolean
language sql
stable
security definer
set search_path = public, pg_temp
as $$
  select exists (
    select 1 from public.profiles
    where id = requested_user_id and status = 'active'
  );
$$;

create or replace function private.is_admin(requested_user_id uuid default auth.uid())
returns boolean
language sql
stable
