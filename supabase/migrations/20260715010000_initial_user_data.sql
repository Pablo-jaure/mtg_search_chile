create or replace view public.admin_user_summary
with (security_invoker = true)
as
select
  p.id,
  p.email,
  p.display_name,
  p.avatar_url,
  p.provider,
  p.role,
  p.status,
  p.last_seen_at,
  p.deletion_requested_at,
  p.created_at,
  count(distinct w.id) as wishlist_count,
  count(distinct f.store_id) as favorite_store_count,
  count(distinct s.id) as search_count,
  count(distinct c.id) as cart_event_count
from public.profiles p
left join public.wishlist_items w on w.user_id = p.id
left join public.favorite_stores f on f.user_id = p.id
left join public.search_runs s on s.user_id = p.id
left join public.cart_events c on c.user_id = p.id
group by p.id;

revoke all on public.admin_user_summary from public, anon, authenticated;
grant select on public.admin_user_summary to service_role;

create or replace function private.purge_expired_user_data()
returns table (deleted_searches bigint, deleted_cart_events bigint)
language plpgsql
security definer
set search_path = public, pg_temp
as $$
declare
  searches_count bigint;
  carts_count bigint;
begin
  delete from public.search_runs where created_at < now() - interval '90 days';
  get diagnostics searches_count = row_count;

  delete from public.cart_events where created_at < now() - interval '12 months';
  get diagnostics carts_count = row_count;

  return query select searches_count, carts_count;
end;
$$;

revoke all on function private.purge_expired_user_data() from public, anon, authenticated;

do $$
declare
  existing_job bigint;
begin
  select jobid into existing_job from cron.job where jobname = 'purge-mtg-user-history';
  if existing_job is not null then
    perform cron.unschedule(existing_job);
  end if;
  perform cron.schedule(
    'purge-mtg-user-history',
    '17 4 * * *',
    'select * from private.purge_expired_user_data()'
  );
end
$$;

