create or replace function private.handle_new_user()
returns trigger
language plpgsql
security definer
set search_path = public, private, pg_temp
as $$
declare
  configured_admin text;
  assigned_role public.app_role := 'user';
begin
  select lower(value) into configured_admin
  from private.app_config
  where key = 'initial_admin_email';

  if configured_admin is not null and lower(new.email) = configured_admin then
    assigned_role := 'admin';
  end if;

  insert into public.profiles (
    id, email, display_name, avatar_url, provider, role, last_seen_at
  ) values (
    new.id,
    new.email,
    coalesce(new.raw_user_meta_data ->> 'full_name', new.raw_user_meta_data ->> 'name', split_part(new.email, '@', 1)),
    coalesce(new.raw_user_meta_data ->> 'avatar_url', new.raw_user_meta_data ->> 'picture'),
    coalesce(new.raw_app_meta_data ->> 'provider', 'google'),
    assigned_role,
    now()
  )
  on conflict (id) do update set
    email = excluded.email,
    display_name = coalesce(public.profiles.display_name, excluded.display_name),
    avatar_url = coalesce(excluded.avatar_url, public.profiles.avatar_url),
    provider = excluded.provider,
    last_seen_at = now();

  return new;
end;
$$;

drop trigger if exists on_auth_user_created on auth.users;
create trigger on_auth_user_created
after insert or update of email, raw_user_meta_data, raw_app_meta_data on auth.users
for each row execute function private.handle_new_user();

